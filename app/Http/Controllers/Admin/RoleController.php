    public function allRoles()
    {
        abort_unless(auth()->user()->hasPermissionTo('view roles') , 403);
        $roles = Role::query();
        if(!auth()->user()->isStaffMember()){
            $roles->whereVisible(true);
        }
        return $roles->get();
